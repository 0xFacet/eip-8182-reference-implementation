import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { ethers } from 'ethers'

const __filename = fileURLToPath(import.meta.url)
const __dirname = dirname(__filename)
const repoRoot = resolve(__dirname, '..')

const FIELD_MODULUS =
  21888242871839275222246405745257275088548364400416034343698204186575808495617n

const DOMAIN_CONTEXTS = {
  OWNER_COMMITMENT_DOMAIN: 'owner_commitment',
  NOTE_BODY_COMMITMENT_DOMAIN: 'note_body_commitment',
  NOTE_COMMITMENT_DOMAIN: 'note_commitment',
  NULLIFIER_DOMAIN: 'nullifier',
  PHANTOM_NULLIFIER_DOMAIN: 'phantom_nullifier',
  INTENT_REPLAY_ID_DOMAIN: 'intent_replay_id',
  OWNER_NULLIFIER_KEY_HASH_DOMAIN: 'owner_nullifier_key_hash',
  TRANSACT_NOTE_SECRET_DOMAIN: 'transact_note_secret',
  TRANSACTION_INTENT_DIGEST_DOMAIN: 'transaction_intent_digest',
  OUTPUT_BINDING_DOMAIN: 'output_binding',
  AUTH_POLICY_DOMAIN: 'auth_policy',
  AUTH_POLICY_KEY_DOMAIN: 'auth_policy_key',
  AUTH_VK_DOMAIN: 'auth_vk',
  NOTE_SECRET_SEED_DOMAIN: 'note_secret_seed',
  USER_REGISTRY_LEAF_DOMAIN: 'user_registry_leaf',
}

function deriveDomain(contextName) {
  const hash = BigInt(ethers.utils.keccak256(ethers.utils.toUtf8Bytes(`eip-8182.${contextName}`)))
  return hash % FIELD_MODULUS
}

function renderTs() {
  const domainAssignments = Object.entries(DOMAIN_CONTEXTS)
    .map(
      ([name]) =>
        `export const ${name} = deriveEip8182Domain(DOMAIN_CONTEXTS.${name})`,
    )
    .join('\n')

  const domainContextEntries = Object.entries(DOMAIN_CONTEXTS)
    .map(([name, context]) => `  ${name}: '${context}',`)
    .join('\n')

  return `// This file is generated by scripts/generate-domain-constants.mjs.
// Do not edit it by hand.

import { keccak_256 } from '@noble/hashes/sha3'
import { utf8ToBytes } from '@noble/hashes/utils'

export const FIELD_MODULUS =
  ${FIELD_MODULUS}n

export const DOMAIN_CONTEXTS = {
${domainContextEntries}
} as const

function bytesToBigInt(bytes: Uint8Array): bigint {
  let result = 0n
  for (const byte of bytes) {
    result = (result << 8n) | BigInt(byte)
  }
  return result
}

export function deriveEip8182Domain(contextName: string): bigint {
  return bytesToBigInt(keccak_256(utf8ToBytes(\`eip-8182.\${contextName}\`))) % FIELD_MODULUS
}

${domainAssignments}
`
}

function renderNoir() {
  const domainLines = Object.entries(DOMAIN_CONTEXTS)
    .map(([name, context]) => `pub global ${name}: Field = ${deriveDomain(context)};`)
    .join('\n')

  return `// This file is generated by scripts/generate-domain-constants.mjs.
// Do not edit it by hand.

pub global PRIVACY_POOL_ADDRESS: Field = 0x81820;
pub global COMMITMENT_TREE_DEPTH: u32 = 32;
pub global USER_TREE_DEPTH: u32 = 160;
pub global AUTH_POLICY_TREE_DEPTH: u32 = 160;

pub global TRANSFER_OP: Field = 0;
pub global WITHDRAWAL_OP: Field = 1;

pub global ADDRESS_BITS: u32 = 160;
pub global AMOUNT_BITS: u32 = 248;
pub global VALID_UNTIL_BITS: u32 = 32;
pub global EXECUTION_CONSTRAINTS_FLAGS_BITS: u32 = 32;

${domainLines}
pub global LOCK_OUTPUT_BINDING_0: Field = 1;
pub global LOCK_OUTPUT_BINDING_1: Field = 2;
pub global LOCK_OUTPUT_BINDING_2: Field = 4;

pub global DUMMY_OWNER_NULLIFIER_KEY_PREIMAGE: Field = 0xdead;
`
}

const targets = [
  {
    path: resolve(repoRoot, 'src/lib/domainConstants.ts'),
    contents: renderTs(),
  },
  {
    path: resolve(repoRoot, 'circuits/common/src/constants.nr'),
    contents: renderNoir(),
  },
]

const checkOnly = process.argv.includes('--check')
let mismatch = false

for (const target of targets) {
  if (checkOnly) {
    const current = readFileSync(target.path, 'utf8')
    if (current !== target.contents) {
      mismatch = true
      process.stderr.write(`out of date: ${target.path}\n`)
    }
    continue
  }

  mkdirSync(dirname(target.path), { recursive: true })
  writeFileSync(target.path, target.contents)
  process.stdout.write(`wrote ${target.path}\n`)
}

if (checkOnly && mismatch) {
  process.exit(1)
}
